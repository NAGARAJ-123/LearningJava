package airthmeticoperator;
public class AreaofCircumference {
	public static void main(String[] args) {
     
     //Circumference of circle
     int r1=5;
     double c2=2*3.14*r1;
     System.out.println(c2);
     
    //Circumference of square=4*a
     int s1=5;
     int s=4*s1;
     System.out.println(s);
	
	
	//Circumference of rectangle=2(l+w)
    int l1=5;
    int w=9;
    int r2=2*(l1+w);
    System.out.println(r2);
	}


}
